<script>
    $('.xulydonhang').change(function() {
        const value = $(this).val();
        const order_code = $(this).find(':selected').attr('id');
        if (value == 1) {
            alert('Vui lòng chọn tình trạng xử lý');
        } else {
            $.ajax({
                method: 'POST',
                url: "/Order/process",
                data: {
                    value: value,
                    order_code: order_code,
                },
                success: function() {
                    alert('Thay đổi tình trạng đơn hàng thành công');
                }
            })
        }
    })
</script>

